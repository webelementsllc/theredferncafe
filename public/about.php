		<h2>About Me</h2>
		<p>I have been working in the food and hospitality industry since I was 15 years old in both corporate and small business. I’ve been a lead cake decorator, and a baking assistant to a French-trained pastry chef, and now opening a cafe of my own. Those years have taught me to truly value small business because they are the ones with heart, passion, and integrity.</p>
		<p>I am also an artist who dabbles in pottery as well as mixed media. I love to challenge myself with new adventures, whether that is sculpting a cake, trying a new recipe or flavor combination, or exploring a new art medium.</p>
		<p>My home has always been a sanctuary for my friends and family where they can come relax, create, and/or recharge. I hope to bring a similar atmosphere to the cafe where customers can feel at home.</p>
