			<h2>Who's Fern?</h2>
			<p>The cafe is named after my grandmother Fern who was known for her red hair. My nieces and nephews used to call her “Gamma Goffee” because she always asked for a cup of coffee to sip when she arrived. She stayed busy dancing polka, volunteering, and playing 500 every chance she got. She was a constant in my life. Every milestone I had, she was there because family was so important to her. I always knew I had a place in her home because wherever she lived, the guest bedroom was called “Julie’s Room.” I even lived with her for a time after college, and she said her fondest memories were the mornings where I would make us a breakfast of coffee and waffles. This cafe is my way of showing her that she will always have a place in my heart because I couldn't have done it without her support throughout my life.</p>
